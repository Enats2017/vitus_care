	public function autocomplete() {
		$json = array();

		if (isset($this->request->get['filter_heading']) || isset($this->request->get['filter_auther'])) {
			$this->load->model('catalog/product');
			if (isset($this->request->get['filter_heading'])) {
				$filter_heading = $this->request->get['filter_heading'];
			} else {
				$filter_heading = '';
			}
			if (isset($this->request->get['filter_auther'])) {
				$filter_auther = $this->request->get['filter_auther'];
			} else {
				$filter_auther = '';
			}
			if (isset($this->request->get['limit'])) {
				$limit = (int)$this->request->get['limit'];
			} else {
				$limit = 5;
			}
			$filter_data = array(
				'filter_heading'  => $filter_heading,
				'filter_auther' => $filter_auther,
				'start'        => 0,
				'limit'        => $limit
			);
			$results = $this->model_catalog_product->getProducts($filter_data);
			foreach ($results as $result) {
				$option_data = array();
				$json[] = array(
					'id' => $result['id'],
					'heading'       => strip_tags(html_entity_decode($result['heading'], ENT_QUOTES, 'UTF-8')),
					'author'      => $result['author']
				);
			}
		}

		$this->response->addHeader('Content-Type: application/json');
		$this->response->setOutput(json_encode($json));
	}
